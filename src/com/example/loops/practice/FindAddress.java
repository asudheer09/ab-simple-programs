package com.example.loops.practice;

public class FindAddress {

    public static void main(String[] args) {
        int streetNumber=8;

        if(streetNumber==3 || streetNumber==5 ){  //true or false= true
            System.out.println("distribute sweets");
        }else{
            System.out.println("distribute bombs");
        }

      /*

        true || true  ===> true
        true || false  ===> true
        false || true  ===> true
        false || false  ===> false


        true && true  ===> true
        true && false  ===> false
        false && true  ===> false
        false && false  ===> false
      */
    }
}
