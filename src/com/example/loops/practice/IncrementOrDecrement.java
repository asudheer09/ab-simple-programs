package com.example.loops.practice;

public class IncrementOrDecrement {

    public static void main(String[] args) {
       int x=15;

        System.out.println("original value of x is "+x); //15

        x++;

        System.out.println("x++ value is "+x);

        ++x;

        System.out.println("++x value  is "+x);



    }
}
